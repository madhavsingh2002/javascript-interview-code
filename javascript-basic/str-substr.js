const text = "Hello, World!";

// Extract a substring starting from index 7 and including 5 characters
const substring1 = text.substr(7, 5);

console.log(substring1); // Output: "World"

// Extract a substring starting from index 0 to the end of the string
const substring2 = text.substr(0);

console.log(substring2); // Output: "Hello, World!"
