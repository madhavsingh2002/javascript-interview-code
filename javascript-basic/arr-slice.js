// Creating an array of letters
const letters = ['a', 'b', 'c', 'd', 'e'];

// Using slice() to extract a portion of the array
const slicedArray = letters.slice(1, 4);

console.log(slicedArray); // Output: ["b", "c", "d"]
console.log(letters);     // Output: ["a", "b", "c", "d", "e"] (original array is unchanged)
