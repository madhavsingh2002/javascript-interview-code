// Creating an array of colors
const colors = ['red', 'green', 'blue', 'yellow'];

// Using join() with a default separator (comma)
const result1 = colors.join();
console.log(result1); // Output: "red,green,blue,yellow"

// Using join() with a custom separator
const result2 = colors.join(' - ');
console.log(result2); // Output: "red - green - blue - yellow"
