// Creating an array of fruits
const fruits = ['apple', 'banana', 'cherry'];

// Using push() to add elements to the end of the array
const newLength = fruits.push('date', 'grape');

console.log(newLength); // Output: 5 (the new length of the array)
console.log(fruits);   // Output: ["apple", "banana", "cherry", "date", "grape"]
