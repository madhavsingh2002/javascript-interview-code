const text = "Hello, World!";

// Get the character at index 0 (the first character)
const firstChar = text.charAt(0);
console.log(firstChar); // Output: "H"

// Get the character at index 7 (the comma)
const commaChar = text.charAt(7);
console.log(commaChar); // Output: ","

// Get the character at an out-of-range index
const outOfRangeChar = text.charAt(15);
console.log(outOfRangeChar); // Output: ""
